    private void jb_MSAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_MSAddActionPerformed
        // TODO add your handling code here:
        if(SingletonPattern.getInstance().getSystem().getStore().getCustomerFromCnic(jTextField_CustomerCNIC.getText())==-1){
            JOptionPane.showMessageDialog(null, "Invalid Customer ID");
        }
        else{
            int itemIndex = SingletonPattern.getInstance().getSystem().getStore().getItemQuantityFromSku(jtf_MSItemSku.getText());
            float price = SingletonPattern.getInstance().getSystem().getStore().getItemQuantityList().get(itemIndex).getItem().getPrice();
            int totalQuantity = SingletonPattern.getInstance().getSystem().getStore().getItemQuantityList().get(itemIndex).getQuantity();
            int quantity = (Integer)jS_MSQuantity.getValue();
            float total=price*quantity;
            if(quantity>totalQuantity){
                JOptionPane.showMessageDialog(null, "Insufficient Quantity Available");
            }
            else{

                if(validateFields()){
                    model.insertRow(jTable_MakeSale.getRowCount(), new Object[]{
                        jtf_MSItemSku.getText() ,
                        jS_MSQuantity.getValue(),
                        total
                    });
                }
            }
            resetFields();
        }
    }//GEN-LAST:event_jb_MSAddActionPerformed


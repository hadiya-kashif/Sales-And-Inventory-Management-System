    private void jToggleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton2ActionPerformed
        // TODO add your handling code here:
        if(jTextField_CustomerCNIC.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Specify Customer");
        }
        else if(SingletonPattern.getInstance().getSystem().getStore().getCustomerFromCnic(jTextField_CustomerCNIC.getText())==-1){
            JOptionPane.showMessageDialog(null, "Invalid Customer ID");
        }
        else{
            Store store = SingletonPattern.getInstance().getSystem().getStore();
            int customerIndex = store.getCustomerFromCnic(jTextField_CustomerCNIC.getText());
            int cashierIndex = store.getCurrentCashier();
            Customer customer = store.getCustomerList().get(customerIndex);
            float categoryDiscount = customer.calculateDiscount();
            Sale sale = new Sale(store.getSaleList().size()+1, store.getCustomerList().get(customerIndex), store.getCashierList().get(cashierIndex),0);
            float totalDiscount=0;
            for(int i=0; i<jTable_MakeSale.getRowCount(); i++){
                int itemIndex = store.getItemFromSku(jTable_MakeSale.getValueAt(i, 0).toString());
                int itemQuantityIndex = store.getItemQuantityFromSku(jTable_MakeSale.getValueAt(i, 0).toString());
                
                int quantity = (Integer)jTable_MakeSale.getValueAt(i, 1);
                
                ItemQuantity item = new ItemQuantity(store.getItemL().get(itemIndex), quantity);
                sale.addItem(item);
                float discount = item.getItem().getDiscount();
                if(discount!=0){
                    totalDiscount += (discount/100 * item.getItem().getPrice()*quantity);
                    if(categoryDiscount!=0){
                         totalDiscount += (categoryDiscount/100 * item.getItem().getPrice());
                    } 
                }
                store.getItemQuantityList().get(itemQuantityIndex).decreaseQuantity(quantity);
            }
            
            
            sale.setDiscount(totalDiscount);
            SingletonPattern.getInstance().getSystem().getStore().getSaleList().add(sale);
            JOptionPane.showMessageDialog(null, "Discount: "+totalDiscount+'\n'+"Receipt id: "+sale.getSaleID());
            
            
            if(customer.gettype()==0){
                if(store.getCustomerSales(customerIndex)>=5){
                    Gold newCustomer = new Gold(customer.getAddress(), customer.getName(),customer.getCNIC(), customer.getContactNo() );
                    store.getCustomerList().set(customerIndex, newCustomer);
                     JOptionPane.showMessageDialog(null, "Customer Category Changed to Gold");
                }
            }
            else if(customer.gettype()==1){
                if(store.getCustomerSales(customerIndex)>=10){
                    Platinum newCustomer = new Platinum(customer.getAddress(), customer.getName(),customer.getCNIC() , customer.getContactNo());
                    store.getCustomerList().set(customerIndex, newCustomer);
                     JOptionPane.showMessageDialog(null, "Customer Category Changed to Platinum");
                }
            }
        }
          model.setRowCount(0);
    }//GEN-LAST:event_jToggleButton2ActionPerformed


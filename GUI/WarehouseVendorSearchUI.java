    private void jToggleButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton3ActionPerformed
        // TODO add your handling code here:
       
                 jTf_VendorName.setText("");
                 jTf_VendorContact.setText("");
                 jTf_VendorCompany.setText("");
        int flag=-1;
        String VendCNIC=jTf_VendorID.getText();
        List<Vendor> Vendarr=SingletonPattern.getInstance().getSystem().getWarehouse().getVendorList();
        for(int i=0;i<Vendarr.size();i++){
            
            if(Vendarr.get(i).getCNIC().equals(VendCNIC)){  
                flag=1;
                 jTf_VendorName.setText(Vendarr.get(i).getName());
                 jTf_VendorContact.setText(Vendarr.get(i).getContactNo());
                 jTf_VendorCompany.setText(Vendarr.get(i).getCompanyName());
               
            }
            
        }
        if(flag==-1){
            JOptionPane.showMessageDialog(WarehouseVendorSearchUI.this, "No Result Found");
            jTf_VendorID.setText("");
            
        }
    }//GEN-LAST:event_jToggleButton3ActionPerformed


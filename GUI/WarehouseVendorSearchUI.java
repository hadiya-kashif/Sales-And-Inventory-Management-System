    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        if(jTb_Vendors.getRowCount()>0)
        model.setRowCount(0);
        int flag=-1;
        String Vendor=jTf_VendorInput.getText();
        List<Vendor> Vendarr=SingletonPattern.getInstance().getSystem().getWarehouse().getVendorList();
        for(int i=0;i<Vendarr.size();i++){

            if(Vendarr.get(i).getName().contains(Vendor)){

                model.insertRow(jTb_Vendors.getRowCount(),new Object[]{
                    Vendarr.get(i).getCNIC(),
                    Vendarr.get(i).getName(),
                    Vendarr.get(i).getContactNo(),
                    Vendarr.get(i).getCompanyName()

                });
                flag=1;

            }

        }
        if(flag==-1){
            JOptionPane.showMessageDialog(WarehouseVendorSearchUI.this, "No Result Found");
            jTf_VendorInput.setText("");

        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed


    private void jToggleButton_ItemCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton_ItemCheckActionPerformed
        // TODO add your handling code here:
        if (jTextField_ItemSKU.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Specify SKU");
        } else {
            int itemIndex = SingletonPattern.getInstance().getSystem().getWarehouse().getItemFromSKU(jTextField_ItemSKU.getText());
            if (itemIndex == -1) {
                JOptionPane.showMessageDialog(null, "Item not found");
            } else {
                jTextField_ItemName.setEnabled(true);
                jTextField_ItemPrice.setEnabled(true);
                jComboBox1.setEnabled(true);
                Item item = SingletonPattern.getInstance().getSystem().getWarehouse().getItemList().get(itemIndex);
                jTextField_ItemName.setText(item.getName());
                jTextField_ItemPrice.setText("" + item.getPrice());
            }
        }
    }//GEN-LAST:event_jToggleButton_ItemCheckActionPerformed

    private void jToggleButton_BatchCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton_BatchCheckActionPerformed
        // TODO add your handling code here:
        if (jTextField_BatchID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Specify Batch ID");
        } else {
            int batchID = Integer.parseInt(jTextField_BatchID.getText());
            int batchIndex = SingletonPattern.getInstance().getSystem().getWarehouse().getBatchFromID(batchID);
            if (batchIndex == -1) {
                JOptionPane.showMessageDialog(null, "Batch not found");
            } else {
                jTextField_VendorID.setEnabled(true);
                jSpinner_Items.setEnabled(true);
                jSpinner_BoxNum.setEnabled(true);
                Batch batch = SingletonPattern.getInstance().getSystem().getWarehouse().getBatchList().get(batchIndex);
                jTextField_VendorID.setText(batch.getVendor().getCNIC());
                if (batch.getItem().isPerishable() == 1) {
                    jTextField_Expiry.setEnabled(true);
                    jTextField_Expiry.setText(batch.getExpiry());
                }

            }
        }
    }//GEN-LAST:event_jToggleButton_BatchCheckActionPerformed


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int batchID = Integer.parseInt(jTextField_BatchID.getText());
        int batchIndex = SingletonPattern.getInstance().getSystem().getWarehouse().getBatchFromID(batchID);

        if (batchID == -1) {
            JOptionPane.showMessageDialog(null, "Invalid Batch ID");
        } 
        else {
            int vendorIndex = SingletonPattern.getInstance().getSystem().getWarehouse().getVendorFromCNIC(jTextField_VendorID.getText());
            if (vendorIndex == -1) {
                JOptionPane.showMessageDialog(null, "Invalid Vendor ID");
            } 
            else {
                if ((Integer) jSpinner_Items.getValue() == 0) {
                    JOptionPane.showMessageDialog(null, "Invalid Items Per Box");
                } 
                else if ((Integer) jSpinner_BoxNum.getValue() == 0) {
                    JOptionPane.showMessageDialog(null, "Invalid Items Per Box");
                } 
                else {
                    SingletonPattern.getInstance().getSystem().getWarehouse().updateBatch(batchIndex, vendorIndex, jTextField_Expiry.getText(), (Integer) jSpinner_BoxNum.getValue(), (Integer) jSpinner_Items.getValue());
                    JOptionPane.showMessageDialog(null, "Inventory Updated!");
                    jTextField_VendorID.setText("");
                    jTextField_Expiry.setText("");
                }
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        List<Item> itemList = SingletonPattern.getInstance().getSystem().getWarehouse().getItemList();
        if (jTextField_ItemSKU.getText().isEmpty())
            JOptionPane.showMessageDialog(null, "Specify SKU");
        else if (SingletonPattern.getInstance().getSystem().getWarehouse().getItemFromSKU(jTextField_ItemSKU.getText()) == -1)
            JOptionPane.showMessageDialog(null, "Invalid SKU");
        else {
            int itemIndex = SingletonPattern.getInstance().getSystem().getWarehouse().getItemFromSKU(jTextField_ItemSKU.getText());
            float price = Float.parseFloat(jTextField_ItemPrice.getText());
            String department = jComboBox1.getSelectedItem().toString();
            SingletonPattern.getInstance().getSystem().getWarehouse().updateItem(itemIndex, jTextField_ItemName.getText(), price, department);
            JOptionPane.showMessageDialog(null, "Inventory Updated!");
            jTextField_ItemName.setText("");
            jTextField_ItemPrice.setText("");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

